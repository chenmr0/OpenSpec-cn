/**
 * Propose Workflow Template
 *
 * Interactive brainstorming + spec/design/plan workflow.
 * Integrates Superpowers brainstorming and writing-plans approaches
 * into the OpenSDD artifact-driven framework.
 */
import type { SkillTemplate, CommandTemplate } from '../types.js';

const proposeInstructions = `提案新变更 - 通过头脑风暴创建变更并生成交互式产出物。

我将引导您完成一个协作设计流程：
1. 保存您的需求描述
2. 探索项目上下文并提出澄清问题
3. 提出 2-3 种设计方案并让您选择
4. 编写规范 (spec.md) 并让您审查
5. 编写设计文档 (design.md) 并让您审查
6. 生成实现计划 (plan.md)

准备好实现后，运行 /sdd/apply

---

<HARD-GATE>
在你展示设计方案并获得用户批准之前，不要编写任何代码、搭建任何项目或采取任何实现行动。
</HARD-GATE>

**输入**：用户的请求应包含变更名称（kebab-case）或对他们想要构建内容的描述。

**步骤**

1. **如果没有提供明确的输入，询问他们想要构建什么**

   使用 **AskUserQuestion tool**（开放式，无预设选项）询问：
   > "您想要处理什么变更？请描述您想要构建或修复的内容。"

   根据他们的描述，推导出一个 kebab-case 名称（例如："add user authentication" → \`add-user-auth\`）。

   **重要提示**：在不了解用户想要构建什么的情况下，请勿继续。

2. **创建变更目录**
   \`\`\`bash
   opensdd new change "<name>"
   \`\`\`
   这将在 \`openspec/changes/<name>/\` 创建一个带有 \`.openspec.yaml\` 的脚手架变更。

3. **保存 proposal.md**

   将用户的原始需求描述直接写入 \`openspec/changes/<name>/proposal.md\`。
   不要修改、完善或重新解释用户的描述。直接保存其原文。

   写入后验证文件存在，然后继续。

4. **头脑风暴：探索项目上下文**

   从用户的描述中提取关键词，在代码仓中进行定向搜索：

   - **关键词定向搜索**：搜索用户提到的功能名、技术术语、领域概念相关的实现
   - **现有模式识别**：看代码仓中有没有类似的功能可以参考——数据模型、API 风格、组件结构、错误处理模式
   - **已有文档查找**：检查 \`docs/\`、\`specs/\`、\`openspec/specs/\` 下是否有相关的规范/设计文档
   - **技术栈理解**：从 \`package.json\`、\`Cargo.toml\`、\`go.mod\` 等了解依赖和框架，从目录结构了解架构风格
   - **影响范围分析**：搜索哪些文件会受用户请求影响——哪些需要修改、哪些需要新增、哪些需要保持兼容

   探索完成后，你应该已经知道：技术栈是什么、相关代码在哪、现有模式是什么、影响范围多大。这些都不需要问用户。

   **范围检查**：如果需求描述了多个独立子系统（例如"构建一个包含聊天、文件存储、计费和分析的平台"），立即指出这一点。不要花时间用问题去细化一个需要先拆分的项目。帮助用户分解为子项目。

5. **头脑风暴：提出澄清问题**

   核心原则：**把代码能告诉你的当作已知，把只有用户能告诉你的当作问题。**

   探索完项目上下文后，先在脑中形成假设，再用问题去验证或补全。不是空问"你想怎么做"，而是：

   - "我看到代码中已经有 X 模式用于处理 [类似场景]，你希望新功能遵循同样的模式吗？" — **假设 + 验证**，而非开放式提问
   - "代码仓中没有看到 [某功能] 的现有实现，这是需要从零构建的部分。你对它的优先级如何排列？" — **指出你看到的空白**，让用户做取舍
   - "现有的 Y 组件做了 [某事]，但它没有覆盖 [你的场景]。你希望扩展 Y 还是为新场景创建独立组件？" — **给出基于代码的选项**，而非凭空想象

   **四类只有用户能回答的问题：**

   1. **意图（为什么）** — 用户想要达到什么业务目标？解决什么人的什么痛点？
   2. **业务规则（做什么）** — 导出什么格式？多大体量？谁有权限？频率如何？
   3. **优先级取舍（选什么）** — 更快的交付 vs 更完善的方案？兼容旧接口 vs 干净的新设计？
   4. **外部约束（限制什么）** — 第三方 API 限制、部署环境、时间线、合规要求等

   **避免的低质量问题：**
   - 代码仓里能直接看到答案的（"你用的什么框架？"）
   - 太宽泛无法聚焦的（"你有什么要求？"）
   - 用户已经在 prompt 中说过的（反复确认已提供的信息）

   使用 **AskUserQuestion tool** 提出每个问题。

6. **头脑风暴：提出 2-3 种方案**

   提出 2-3 种不同的方案及其权衡。先展示你推荐的方案并解释原因。以对话的方式展示选项。

7. **头脑风暴：分节展示设计**

   一旦你认为理解了要构建的内容：
   - 按复杂度分节展示设计，每节的篇幅与其复杂度匹配
   - 简单的几句话，复杂的最多 200-300 字
   - 每个部分展示后询问是否正确
   - 涵盖：架构、组件、数据流、错误处理、测试
   - 随时准备回头澄清不明确的地方

   **设计原则**：
   - 将系统拆分为更小的单元，每个单元有一个明确的职责，通过定义良好的接口通信，可以独立理解和测试。
   - 对于每个单元，你应该能回答：它做什么，如何使用，它依赖什么？
   - 别人能否不看内部实现就理解一个单元的功能？你能否在不影响调用者的情况下修改内部实现？如果不能，边界需要调整。
   - 更小、边界清晰的单元也更便于你工作——你对能一次放入上下文的代码推理得更好，文件越专注你的编辑越可靠。当文件变大时，这通常意味着它承担了过多职责。

   使用 **AskUserQuestion tool** 获取用户对每节的批准。如果用户要求修改，迭代修改。

8. **创建 specs 产出物**

   用户批准设计后，创建规范：

   a. 获取指令：
   \`\`\`bash
   opensdd instructions specs --change "<name>" --json
   \`\`\`

   b. 从头脑风暴中提取 WHAT（系统应该做什么），编写规范文件：
   - 将所有需求规范写入 \`spec.md\`，按能力分区组织
   - 每个需求使用 \`### 需求:<名称>\` 后跟描述（使用 SHALL/MUST 表达强制性需求）
   - 每个需求必须至少有一个 WHEN/THEN 场景：\`#### 场景:<名称>\` + \`**当** ...\` + \`**那么** ...\`
   - **关键**：场景标题必须恰好使用四个井号 (\`####\`)

   c. **执行规范自检**：
   1. 场景完整性：每个需求是否至少有一个 WHEN/THEN 场景？
   2. 可测试性：每个场景是否足够具体，可以转化为测试用例？
   3. 内部一致性：各需求之间有矛盾吗？
   4. 范围检查：是否聚焦到可以用一个实现计划覆盖？
   5. 模糊性检查：有没有需求可以被两种方式理解？

   发现问题时直接内联修复。无需重新审查——修好继续推进。

   d. **请用户审查规范**：
   > "规范文档已写入 \`spec.md\`。请审查一下，如果在我们继续编写设计文档之前你想做任何修改，请告诉我。"

   使用 **AskUserQuestion tool** 等待用户回复。如果他们要求修改，做出修改并重新运行自检。只有在用户批准后才继续。

   e. 显示简短进度："✓ 已创建 specs"

9. **创建 design 产出物**

   规范获得用户批准后，创建设计：

   a. 获取指令：
   \`\`\`bash
   opensdd instructions design --change "<name>" --json
   \`\`\`

   b. 从头脑风暴中提取 HOW（系统如何实现），编写技术设计文档：
   - **上下文**：背景、当前状态、约束
   - **目标 / 非目标**：此设计旨在实现和排除的目标
   - **决策**：关键技术选择及其理由和备选方案
   - **架构**：组件结构、数据流、错误处理
   - **风险 / 权衡**：已知限制和缓解措施

   c. **执行设计自检**：
   1. 需求追溯：每个设计决策是否可以追溯到 SPEC 中的某个需求？
   2. 占位符扫描：有没有"待定"、"TODO"、未完成的章节？修复它们。
   3. 内部一致性：架构和决策是否匹配？组件结构与数据模型是否一致？
   4. 孤立设计：有没有设计元素没有对应的需求支撑？如果有，移除或补充需求。
   5. 可构建性：工程师能否基于此设计编写实现计划？

   发现问题时直接内联修复。无需重新审查——修好继续推进。

   d. **请用户审查设计**：
   > "设计文档已写入 \`design.md\`。请审查一下，如果在我们开始编写实现计划之前你想做任何修改，请告诉我。"

   使用 **AskUserQuestion tool** 等待用户回复。如果他们要求修改，做出修改并重新运行自检。只有在用户批准后才继续。

   e. 显示简短进度："✓ 已创建 design"

10. **创建 plan 产出物**

    设计获得用户批准后，创建实现计划：

    a. 获取指令：
    \`\`\`bash
    opensdd instructions plan --change "<name>" --json
    \`\`\`

    b. 阅读规范（要构建的内容）和设计（如何构建）。然后：

    c. **定义文件结构**：在定义任务之前，列出将要创建或修改的文件以及每个文件的职责。

    d. **创建任务**：将工作分解为小的、可验证的步骤。
       - 将相关任务分组到 ## 编号标题下
       - 每个任务必须是一个复选框：\`- [ ] X.Y 任务描述\`
       - 每个任务必须包含精确的文件路径和完整代码——如果步骤涉及代码变更，就展示代码
       - 任务应该足够小，可以在一次会话中完成
       - 按依赖关系排序任务

    e. **计划文档头部**：以此开始：
    \`\`\`
    # [功能名称] 实现计划

    > 步骤使用复选框 (\`- [ ]\`) 语法来跟踪进度。

    **目标：** [一句话描述要构建什么]
    **架构：** [2-3 句话描述方案]
    **技术栈：** [关键技术/库]
    **规格：** [spec.md 路径]
    **设计：** design.md

    ---
    \`\`\`

    f. **禁止占位符**——以下是计划缺陷，绝不要写出来：
    - "待定"、"TODO"、"后续实现"、"补充细节"
    - "添加适当的错误处理" / "添加验证" / "处理边界情况"（没有具体内容）
    - "为上述代码编写测试"（没有实际测试代码）
    - "类似任务 N"（重复引用——工程师可能不按顺序阅读任务）
    - 只描述做什么而不展示怎么做的步骤（代码步骤必须有代码块）
    - 引用了未在任何任务中定义的类型、函数或方法

    g. **执行计划自检**：
    1. 规格覆盖度：浏览 SPEC 中的每个需求和 WHEN/THEN 场景。你能指出实现它的任务吗？检查每个 WHEN/THEN 场景是否在计划中有对应的验证步骤。列出所有遗漏。
    2. 占位符扫描：搜索计划中的红旗——上方"禁止占位符"章节中的任何模式。修复它们。
    3. 类型一致性：后续任务中使用的类型、方法签名和属性名是否与前面任务中定义的一致？

    如果发现问题，直接内联修复。修复后继续推进。

    h. 显示简短进度："✓ 已创建 plan"

11. **显示最终状态**
    \`\`\`bash
    opensdd status --change "<name>"
    \`\`\`

**输出**

完成所有产出物后，总结：
- 变更名称和位置
- 已创建产出物的列表（proposal、specs、design、plan）及简要描述
- 准备就绪："所有产出物已创建！准备好实现。"
- 提示："运行 \`/sdd/apply\` 开始实现。"

**产出物创建指南**

- 遵循每个产出物类型的 \`opensdd instructions\` 中的 \`instruction\` 字段
- Schema 定义了每个产出物应包含的内容，遵循它
- 在创建新产出物之前阅读依赖产出物以获取上下文
- 使用 \`template\` 作为输出文件的结构 - 填充其各个部分
- **重要提示**：\`context\` 和 \`rules\` 是对你的约束，而不是文件内容
  - 不要将 \`<context>\`、\`<rules>\`、\`<project_context>\` 块复制到产出物中
  - 这些引导你编写内容，但不应出现在输出中

**护栏**
- 提问之前先探索代码库——只问代码无法回答的问题
- 创建实现所需的所有产出物（由 Schema 的 \`apply.requires\` 定义）
- 在创建新产出物之前始终阅读依赖产出物
- 如果同名变更已存在，询问用户是否要继续它或创建一个新的
- 在继续下一个之前，验证写入后每个产出物文件是否存在`;

